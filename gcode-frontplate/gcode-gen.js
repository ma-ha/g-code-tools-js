if ( ! cnc.passes && cnc.passes !== 0 ) { cnc.passes = 4 }
if ( ! cnc.fine ) { cnc.fine = 0.5 }

let mainToolRadius = cnc.ToolDia / 2

console.log(`( >>> ${cnc.PrjName} <<< )`)
console.log(`( Deepth: ${cnc.z} mm )`)
